function getJSON(data) {
    const pieces = data.split("\n");
    const answer = [];
    pieces.forEach((piece) => {
        const split = piece.split("	");
        answer.push({
            time: new Date(split[0]).getTime(),
            val: parseInt(split[1])
        })
    });
    return answer;
}

function getPriceData(data) {
    const answer = [];
    data.forEach((bit) => {
        answer.push({
            time: new Date(bit["t"]).getTime(),
            val: bit["o"]
        });
    });
    return answer;
}

// data should be array of items in this format: {time: number, val: number}
function graphData(data, col, graph) {
    data.sort((a, b) => {
      if (a.time > b.time) {
        return 1;
      } else {
        return -1;
      }
    });
    const numPoints = data.length;
    const pointWidth = 1000.0 / numPoints;
    const maxTime = data[data.length - 1].time;
    const minTime = data[0].time;
    const timeRange = maxTime - minTime;

    let maxVal = 0;
    let minVal = 10000000000;
    data.forEach((bit) => {
        if (bit.val > maxVal) {
            maxVal = bit.val;
        }
        if (bit.val < minVal) {
            minVal = bit.val;
        }
    });

    const valRange = maxVal - minVal;
    let lastVal = data[0].val;

    data.forEach((bit, i) => {
        const positionFraction = 1.0 * (bit.time - minTime) / timeRange;
        const xPos = 1000.0 * positionFraction;
        const heightFraction = 1.0 * (bit.val - minVal) / valRange;
        const height = Math.max(400.0 * heightFraction, 5);
        
        const ele = document.createElement("div");
        ele.classList.add(`${col}Point`);
        ele.style.height = `${height}px`;
        ele.style.left = `${xPos}px`;
        ele.style.width = `${pointWidth}px`;
        ele.id = `${graph}${col}${i}`;
        ele.addEventListener("mouseenter", () => {
            document.getElementById("timestamp").innerHTML = i;
        });
        document.getElementById(graph).appendChild(ele);
        
        
        
        lastVal = xPos;
    });

}

